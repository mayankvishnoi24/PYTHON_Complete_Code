# Comparision operator : (<,>,<=,>=,==,!=) -- it is  showwing the only provide boolean

i = 5
j = 2

print(i>j)# true                (greater than)
print(i<j)# false               (Less than)
print(i>=j)# true               (greater than Equal too)
print(i<=j)# false              (Less than Equal too)
print(i==j)# false              (Equal too equal to)
print(i!=j)# true               (not equal to)
