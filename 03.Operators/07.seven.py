# Bitwise Operators

# Bitwise operators are used to compare (binary) numbers:
# --------------------------------------------------------------------------

# & Bitwise and:
a = 7
b = 4
c = 5
print(a & b)
print(a & c)
print(b & c)

# | Bitwise or:
a = 7
b = 4
c = 5
print(a | b)
print(a | c)
print(b | c)

# ^ Bitwise xor:
a = 7
b = 4
c = 5
print(a ^ c)
print(b ^ c)

