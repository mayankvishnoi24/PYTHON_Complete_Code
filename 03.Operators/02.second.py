# Assignment operator : (=,+=,-=,*=,/=,%=,**=,//=)      it is assigning to the variable of the operator.

i = 5
j = i

print(i,j)# 5 5

i+=j

print(i)# 10

i-=j

print(i)# 5

i*=j

print(i)# 25

i/=j

print(i)# 5.0

i%=j

print(i)# 0.0

i**=j

print(i)# 0.0

i//=j

print(i)# 0.0

